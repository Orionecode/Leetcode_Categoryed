package 快慢指针;

/**
 * @Author YixiaoZeng
 * @Date 2022/2/20 13:19
 * @Filename ListNode
 */
class ListNode {
    int val;
    ListNode next;

    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}
